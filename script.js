function getInitialLanguage() {
	const stored = localStorage.getItem("lang");
	if (stored && translations[stored]) return stored;

	const browserLang = (navigator.language || "").toLowerCase();
	if (browserLang.startsWith("da")) return "da";
	return "en";
}

function applyLanguage(lang) {
	const t = translations[lang] || translations.en;

	document.documentElement.lang = lang;
	localStorage.setItem("lang", lang);

	document.querySelectorAll("[data-i18n]").forEach((el) => {
		const key = el.getAttribute("data-i18n");
		if (!key) return;
		const value = t[key];
		if (typeof value !== "string") return;
		el.textContent = value;
	});

	document.querySelectorAll("[data-i18n-alt]").forEach((el) => {
		const key = el.getAttribute("data-i18n-alt");
		if (!key) return;
		const value = t[key];
		if (typeof value !== "string") return;
		el.setAttribute("alt", value);
	});

	document.querySelectorAll(".lang-btn[data-lang]").forEach((btn) => {
		const btnLang = btn.getAttribute("data-lang");
		btn.setAttribute("aria-pressed", btnLang === lang ? "true" : "false");
	});
}

function setupLanguageSwitch() {
	document.querySelectorAll(".lang-btn[data-lang]").forEach((btn) => {
		btn.addEventListener("click", () => {
			const lang = btn.getAttribute("data-lang");
			if (!lang) return;
			applyLanguage(lang);
		});
	});
}

function setupLanguageItems() {
	const langItems = document.querySelectorAll('.lang-item');
	langItems.forEach(item => {
		item.addEventListener('click', function() {
			const flagEl = this.querySelector('.flag');
			
			// Add click animation
			flagEl.style.animation = 'none';
			setTimeout(() => {
				flagEl.style.animation = '';
			}, 10);
			
			// Show tooltip with language name
			const langName = this.getAttribute('title');
			const tooltip = document.createElement('div');
			tooltip.textContent = langName;
			tooltip.style.cssText = `
				position: fixed;
				background: var(--gradient-primary);
				color: black;
				padding: 6px 12px;
				border-radius: 6px;
				font-size: 12px;
				font-weight: 600;
				pointer-events: none;
				z-index: 1000;
				animation: tooltipFade 0.6s ease-in-out;
				left: 50%;
				top: 50%;
				transform: translate(-50%, -50%);
			`;
			document.body.appendChild(tooltip);
			
			setTimeout(() => {
				tooltip.remove();
			}, 600);
		});
	});
}

function getInitialTheme() {
	const stored = localStorage.getItem("theme");
	if (stored && (stored === "light" || stored === "dark")) return stored;
	
	// Check system preference
	if (window.matchMedia && window.matchMedia("(prefers-color-scheme: light)").matches) {
		return "light";
	}
	return "dark";
}

function applyTheme(theme) {
	if (theme === "light") {
		document.body.classList.add("light-mode");
		localStorage.setItem("theme", "light");
		document.getElementById("theme-toggle").innerHTML = '<i class="fas fa-sun"></i>';
	} else {
		document.body.classList.remove("light-mode");
		localStorage.setItem("theme", "dark");
		document.getElementById("theme-toggle").innerHTML = '<i class="fas fa-moon"></i>';
	}
}

function setupThemeToggle() {
	const themeBtn = document.getElementById("theme-toggle");
	if (!themeBtn) return;
	
	themeBtn.addEventListener("click", () => {
		const isDark = document.body.classList.contains("light-mode");
		applyTheme(isDark ? "dark" : "light");
	});
}

document.addEventListener("DOMContentLoaded", () => {
	setupThemeToggle();
	applyTheme(getInitialTheme());
	setupLanguageSwitch();
	setupLanguageItems();
	applyLanguage(getInitialLanguage());
});
